    private Persona personaRegistrada;//(Variable global - Atributo) -> objeto de la clase Persona
    
    public void crearPersona(){//Creando a la persona para poder registrarlo en la lista 
        try{
            Date fechaIngreso = new Date();//vdsv
            this.personaRegistrada = new Persona(
                    txtfNombre.getText().trim(),
                    txtfApellidos.getText().trim(),
                    txtfContrasenia.getText().trim(),
                    txtfCorreo.getText().trim(),
                    fechaIngreso
            );
        }catch (Exception ex){
            JOptionPane.showMessageDialog(this, "Error al registar la persona");
        }
    }
    
    public Persona obtenerPersonaRegistrada(){//Metodo accesor
        return personaRegistrada;
    }
    
